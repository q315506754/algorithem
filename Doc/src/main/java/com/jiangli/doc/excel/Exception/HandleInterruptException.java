package com.jiangli.doc.excel.Exception;

/**
 * @author JiangLi
 *
 *         CreateTime 2014-12-4 上午11:03:05
 */
public class HandleInterruptException extends Exception {
    /**
     *
     */
    private static final long serialVersionUID = -4916495503488179888L;

    /**
     *
     */
    public HandleInterruptException() {
        super();
    }

    /**
     * @param message
     * @param cause
     */
    public HandleInterruptException(String message, Throwable cause) {
        super(message, cause);
    }

    /**
     * @param message
     */
    public HandleInterruptException(String message) {
        super(message);
    }

    /**
     * @param cause
     */
    public HandleInterruptException(Throwable cause) {
        super(cause);
    }


}
